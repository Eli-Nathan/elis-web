<h3>1: Omit certain results</h3>
          
    <p>This is my most used "Hack" for searching through the millions of results found when performing a search.</p>
<p>You might think: "Why would I not want certain results to come up?"
<br>Well, one example would be:</p>
<p>You may be trying to create a spreadsheet on a certain program other than Excel. However, every time you search for "How to fix my spreadsheet", the results that show will <strong>ALL</strong> be aimed at people using Excel.
    
So to omit all the results that contain the word "Excel" you'd simply type a "<strong>-</strong>" symbol before the word you'd like to get rid of. So now your search would be:</p>
<p>"How to fix my spreadsheet -excel"</p>
