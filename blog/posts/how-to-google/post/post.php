<h3>3: Use quotes to search for an exact phrase</h3>
<p>This is one of the better known tricks and it's really simple.</p>
<p>If you incase your search in quotation marks it will search for that exact phrase! Especially useful for song lyrics, quotes or finding where you read something.</p>
<h3>4: Use an asterisk in a search phrase to represent variable or unknown words</h3>
<p>This one's especially useful if you can't remember part of a quote or you can't quite make out a word in a song lyric.<br>Using an asterisk in the place of a word will search for all variations of that phrase. For example: Searching for "Livin on a *" will show you Bon Jovi's Livin' on a Prayer
