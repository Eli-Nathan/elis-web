<h3>What is guerilla Marketing?</h3>
          
    <p>Geurilla Marketing has been around for year. It's only recently however that it's become popular. It is essentially an advertising strategy which allows the most creative among us to advertise our products at a very low-cost. It usually involves using the landscape or physical structures to incorporate your design into the public eye without having to use conventional advertisements such as billboards and bus shelters.</p>
